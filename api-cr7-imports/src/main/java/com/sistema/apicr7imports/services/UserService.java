import com.sistema.apicr7imports.domain.User;
import com.sistema.apicr7imports.repository.UserRepository;

@Service
public class UserService {

	@Autowired
	private UserRepository repo;
	
	public List<User> findAll(){
		return repo.findAll();
	}
	
}